/* Se puede aumentar y disminuir 1 unidad de un valor dado a una variable de la siguiente manera*/

            // Aumentar el valor de una variable
var aumentar = 2;
aumentar ++;
console.log(aumentar);  // Imprimirá 3

            // Reducir el valor de una variable 
var reducir = 10;
reducir --; 
console.log(reducir); // Imprimirá 9

            // Asignación de suma 
var incremet = 15;
incremet += 10;
console.log(incremet); // Imprimirá 25


            // Asignación de resta
var decrement = 356;
decrement -= 56;
console.log(decrement);// Imprimirá  300


            // Asignación de multiplicación
var mul = 10;
mul *= 5;
console.log(mul); // Imprimirá 50


            // Asignación de división
var div = 120;
div /= 10;
console.log(div); // Imprimirá 12