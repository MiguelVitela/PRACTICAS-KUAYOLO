                    // OPERADOR DE DESIGUALDAD

// Este operador compara 2 valores y retorna true si los valores es diferente y false si son iguales

console.log(9 != 9); // false
console.log(9 != 6); // true
console.log("JavaScript" != "JavaScript"); // false

// NOTA: El operador de sesigualdad al igual que el de igualdad e igualdad estricta tampoco es util para comparar arrays.


                    // OPERADOR DE DESIGUALDAD ESTRICTA (!==)

console.log(54 != "54"); // false
console.log(54 !== "54"); // true

